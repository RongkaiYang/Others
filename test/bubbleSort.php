$s = [99, 89, 3, 34, 45, 2, 78, 35];

$num = count($s);
for ($i = 0; $i < $num; $i++) {
    for ($j = 0; $j < $num - 1 - $i; $j++) {
        if($s[$j] > $s[$j + 1]){
            $a = $s[$j];
            $s[$j] = $s[$j + 1];
            $s[$j + 1] = $a;
            for ($k = 0; $k < $num; $k++){
                echo ',',$s[$k];
            }
            echo '<br>';
        }
    }
}

print_r($s)