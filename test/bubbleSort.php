$t = [3, 56, 4, 7, 45, 2, 99, 23, 6, 29, 1, 33, 78, 5];
for($i = 0; $i < count($t); $i++){
    for ($j = $i + 1; $j < count($t); $j++){
        $a = $t[$i];
        if ($a > $t[$j]){
            $t[$i] = $t[$j];
            $t[$j] = $a;
        }
    }
}
print_r($t);